package clase_07.extra.codigo.dependencia;

public class Libro {

    // Atributos privados
    private String titulo;

    // Constructor
    public Libro(String titulo) {
        this.titulo = titulo;
    }

    // Getters
    public String getTitulo() {
        return this.titulo;
    }

    // Setters
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }
}
